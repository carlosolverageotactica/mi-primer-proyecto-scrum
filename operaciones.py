# Módulo con operaciones matemáticas básicas

def suma(a, b):
    """Devuelve la suma de dos números."""
    return a + b

def resta(a, b):
    """Devuelve la resta de dos números."""
    return a - b

if __name__ == "__main__":
    # Prueba rápida de las funciones
    print(f"Prueba suma 5+3: {suma(5, 3)}")
    print(f"Prueba resta 10-4: {resta(10, 4)}")
    